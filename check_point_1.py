    "        # Defining gradient function.\n",
    "        delta = y_pred - y\n",
    "        # Formula for the weights.\n",
    "        dw = (1 / n) * np.dot(np.transpose(X), (delta))\n",
    "        # Formula for the bias.\n",
    "        db = (1 / n) * np.sum(delta)\n",
    "        return dw, db\n",
